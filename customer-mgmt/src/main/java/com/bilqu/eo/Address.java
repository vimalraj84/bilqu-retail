package com.bilqu.eo;

public class Address{

    private Long id;
    private String street;
    private String city;
    private String county;
    private String state;
    private String zip;
    private String email;
    private String mobilePhNum;
    private String homePhNum;
    private String workPhNum;
    private String faxNum;
    private String webSite;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getCounty() {
        return county;
    }

    public void setCounty(String county) {
        this.county = county;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getZip() {
        return zip;
    }

    public void setZip(String zip) {
        this.zip = zip;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getMobilePhNum() {
        return mobilePhNum;
    }

    public void setMobilePhNum(String mobilePhNum) {
        this.mobilePhNum = mobilePhNum;
    }

    public String getHomePhNum() {
        return homePhNum;
    }

    public void setHomePhNum(String homePhNum) {
        this.homePhNum = homePhNum;
    }

    public String getWorkPhNum() {
        return workPhNum;
    }

    public void setWorkPhNum(String workPhNum) {
        this.workPhNum = workPhNum;
    }

    public String getFaxNum() {
        return faxNum;
    }

    public void setFaxNum(String faxNum) {
        this.faxNum = faxNum;
    }

    public String getWebSite() {
        return webSite;
    }

    public void setWebSite(String webSite) {
        this.webSite = webSite;
    }
}
